package com.imagedownloader.imagedownloader.dto;

public class Image {

    private String name;
    private byte[] body;

    public Image(String name, byte[] body) {
        this.name = name;
        this.body = body;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public byte[] getBody() {
        return body;
    }

    public void setBody(byte[] body) {
        this.body = body;
    }
}
